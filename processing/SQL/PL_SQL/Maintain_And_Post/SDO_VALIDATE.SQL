--CHECK INVALID GEOMETRIES ONLY
SELECT OBJECTID, --MUST BE CHANGED TO REFLECT A UNIQUE IDENTIFIER FIELD
  FC.SHAPE.SDO_SRID                 AS SRID,
  FC.SHAPE.SDO_GTYPE                AS GEOMETRY_TYPE,
  SDO_UTIL.GETNUMVERTICES(FC.SHAPE) AS VERTEX_COUNT,
  SDO_UTIL.GETNUMELEM(FC.SHAPE)     AS PART_COUNT,
  SDO_GEOM.VALIDATE_GEOMETRY_WITH_CONTEXT(FC.SHAPE, X.TOLERANCE),
  SDO_GEOM.SDO_AREA(FC.SHAPE, X.TOLERANCE)                  AS AREA,
  SDO_GEOM.SDO_LENGTH(FC.SHAPE, X.TOLERANCE)                AS LENGTH,
  SDO_GEOM.SDO_MIN_MBR_ORDINATE(FC.SHAPE, M.SDO_DIMINFO, 1) AS MINX,
  SDO_GEOM.SDO_MAX_MBR_ORDINATE(FC.SHAPE, M.SDO_DIMINFO, 2) AS MAXX,
  SDO_GEOM.SDO_MIN_MBR_ORDINATE(FC.SHAPE, M.SDO_DIMINFO, 1) AS MINY,
  SDO_GEOM.SDO_MAX_MBR_ORDINATE(FC.SHAPE, M.SDO_DIMINFO, 2) AS MAXY
FROM SDE.STATES_1 FC, --PLEASE MODIFY THIS VALUE TO MATCH THE SPATIAL TABLE TO VALIDATE
  MDSYS.SDO_GEOM_METADATA_TABLE M,
  (SELECT S.XYCLUSTER_TOL AS TOLERANCE
  FROM SDE.LAYERS L
  JOIN SDE.SPATIAL_REFERENCES S
  ON L.SRID = S.SRID
  WHERE UPPER(L.OWNER
    || '.'
    || L.TABLE_NAME) = UPPER('SDE.STATES_1') --PLEASE MODIFY THIS VALUE TO MATCH THE SPATIAL TABLE TO VALIDATE
  ) X
WHERE SDO_GEOM.VALIDATE_GEOMETRY_WITH_CONTEXT(FC.SHAPE, X.TOLERANCE) <> 'TRUE';